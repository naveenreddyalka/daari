from __future__ import annotations

import shutil
from dataclasses import dataclass, field
from pathlib import Path

from daari.clients.base import SetupChange, SetupPlan
from daari.setup.backup import create_backup, restore_latest_backup

DEFAULT_BASE_URL = "http://127.0.0.1:11435/v1"
DEFAULT_API_KEY = "daari-local"
DEFAULT_MODEL_NAME = "daari"


@dataclass
class ApplyResult:
    changed: bool
    backup_dir: Path | None = None
    files_changed: list[str] = field(default_factory=list)
    message: str = ""


@dataclass
class UndoResult:
    backup_dir: Path
    files_restored: list[str]


class ClaudeCodeSetupRecipe:
    id = "claude-code"

    def detect(self) -> bool:
        return shutil.which("claude") is not None or (Path.home() / ".claude").exists()

    def settings_paths(self) -> list[str]:
        env_file, pointer_file = self._paths()
        return [str(env_file), str(pointer_file)]

    def dry_run(
        self,
        *,
        base_url: str = DEFAULT_BASE_URL,
        api_key: str = DEFAULT_API_KEY,
        model_name: str = DEFAULT_MODEL_NAME,
    ) -> SetupPlan:
        env_file, pointer_file = self._paths()
        detected = self.detect()
        notes = [
            "Dry-run only — no files will be modified.",
            "This recipe writes an env snippet and a config pointer file.",
            "Manual fallback: export OPENAI_BASE_URL/OPENAI_API_KEY before launching claude-code.",
        ]
        changes: list[SetupChange] = []
        for path in (env_file, pointer_file):
            action = "would_patch" if path.exists() else "would_create"
            changes.append(
                SetupChange(
                    path=str(path),
                    action=action,
                    detail=f"Configure claude-code to use {model_name} at {base_url} with API key {api_key}",
                )
            )
        if not detected:
            notes.append("claude-code binary not detected; files can still be generated for later use.")
        return SetupPlan(
            client_id=self.id,
            detected=detected,
            settings_paths=[str(env_file), str(pointer_file)],
            changes=changes,
            notes=notes,
        )

    def apply(
        self,
        *,
        base_url: str = DEFAULT_BASE_URL,
        api_key: str = DEFAULT_API_KEY,
        model_name: str = DEFAULT_MODEL_NAME,
        force: bool = False,
        backup_root: Path | None = None,
    ) -> ApplyResult:
        env_file, pointer_file = self._paths()
        env_content = (
            "# Generated by daari setup claude-code\n"
            f"export OPENAI_BASE_URL={base_url}\n"
            f"export OPENAI_API_KEY={api_key}\n"
            f"export OPENAI_MODEL={model_name}\n"
        )
        pointer_content = (
            "# daari config pointer for claude-code\n"
            f"env_file={env_file}\n"
            "usage=source ~/.claude/daari-openai-compat.env\n"
        )

        unchanged = (
            env_file.is_file()
            and pointer_file.is_file()
            and env_file.read_text(encoding="utf-8") == env_content
            and pointer_file.read_text(encoding="utf-8") == pointer_content
        )
        if unchanged and not force:
            return ApplyResult(changed=False, message="claude-code already configured for daari. Use --force to re-apply.")

        existing = [path for path in (env_file, pointer_file) if path.is_file()]
        backup = create_backup(self.id, existing, root=backup_root) if existing else None

        env_file.parent.mkdir(parents=True, exist_ok=True)
        env_file.write_text(env_content, encoding="utf-8")
        pointer_file.write_text(pointer_content, encoding="utf-8")
        return ApplyResult(
            changed=True,
            backup_dir=backup.backup_dir if backup else None,
            files_changed=[str(env_file), str(pointer_file)],
            message="claude-code env helper written for daari.",
        )

    def undo(self, *, backup_root: Path | None = None) -> UndoResult:
        result = restore_latest_backup(self.id, root=backup_root)
        return UndoResult(backup_dir=result.backup_dir, files_restored=result.files_restored)

    @staticmethod
    def _paths() -> tuple[Path, Path]:
        base = Path.home() / ".claude"
        return (base / "daari-openai-compat.env", base / "daari-config-pointer.txt")
